export const getAllCountries = (): Country[] => {
  return allCountries;
};

// الحصول على تركيا
export const getTurkey = (): Country => {
  return turkeyData;
};

// البحث عن دولة بالمعرف (يشمل جميع الدول)
export const getCountryById = (id: string): Country | undefined => {
  return allCountries.find(country => country.id === id);
};

// البحث عن مدينة بالمعرف
export const getCityById = (countryId: string, cityId: string): City | undefined => {
  const country = getCountryById(countryId);
  return country?.cities.find(city => city.id === cityId);
};

// الحصول على جميع المدن من جميع الدول
export const getAllCities = () => {
  return allCountries.flatMap(country => 
    country.cities.map(city => ({
      ...city,
      country: {
        id: country.id,
        nameAr: country.nameAr,
        nameEn: country.nameEn
      }
    }))
  );
};

// البحث في جميع الوجهات
export const searchDestinations = (query: string) => {
  const normalizedQuery = query.toLowerCase().trim();
  
  const results: {
    countries: Country[];
    cities: (City & { country: { id: string; nameAr: string; nameEn: string } })[];
  } = {
    countries: [],
    cities: []
  };

  allCountries.forEach(country => {
    // Search in country
    if (
      country.nameAr.includes(normalizedQuery) ||
      country.nameEn.toLowerCase().includes(normalizedQuery) ||
      country.description.includes(normalizedQuery)
    ) {
      results.countries.push(country);
    }

    // Search in cities
    country.cities.forEach(city => {
      if (
        city.nameAr.includes(normalizedQuery) ||
        city.nameEn.toLowerCase().includes(normalizedQuery) ||
        city.description.includes(normalizedQuery)
      ) {
        results.cities.push({
          ...city,
          country: {
            id: country.id,
            nameAr: country.nameAr,
            nameEn: country.nameEn
          }
        });
      }
    });
  });

  return results;
};

